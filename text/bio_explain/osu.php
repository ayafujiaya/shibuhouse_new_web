「お好きなものをお選びください展」<br />
日程：2008.10.15 - 10.21<br />
会場：デザインフェスタギャラリー<br />
<br />
mixiや口コミで集まった6人が、ケチャップ•マスタード絵画、発泡スチロール人形、針金造形、コラージュ、写真、映像など、それぞれが好きなものを持ち寄って展示。ほかの丁寧でこぎれいな、ルールに沿った展示とはまったく違う。この展示は常に進化する。チラシやフライヤーという寿命が決まっているものを救おう、という部屋の半分を覆う巨大コラージュ作品。来場者から言葉をもらい、A4の紙に言葉を書き、部屋の壁に壁に貼り続けるパフォーマンス「ジャンクワード」。すべてにおいて同じ状態はない。初日と最終日では、作品量が10倍になったといっても過言ではない。自己主張と自己主張がぶつかり合い、まったく自己主張のない部屋が仕上がる。この部屋は「生きている部屋」だった。
