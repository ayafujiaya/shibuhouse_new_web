「渋家「イケる気がする展」- 第一回渋家アート選手権大会 -」<br />
日程：2013.4.19 - 4.24&nbsp;&nbsp; <br />
会場：HIGURE 17-15 cas<br />
<br />
渋家では2010年より渋家トリエンナーレを開催し、今回、第2回となる「渋家トリエンナーレ 2013」では、その一貫として「渋家アート選手権大会」という渋家メンバーによる公募展を実施した。今回のテーマは「イケる気がする」。 参加条件は、2013年4月中に渋家メンバーであることと「イケる気がする」作品であれば何でも構わない。ただしパフォーマンスに関し特別に時間を設けたりすることはしない。大会優勝者には渋家から「無形の栄誉と賞賛」が贈られた。
